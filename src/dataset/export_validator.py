"""
Export Validator module for the dataset.
"""
from dataclasses import dataclass
from pathlib import Path
import json
import numpy as np
import cv2


@dataclass
class ValidationResult:
    """Stores the validation result for a single scene."""
    scene_id: int
    passed: bool
    errors: list
    warnings: list


class ExportValidator:
    """Validates exported scenes for file existence and data consistency."""
    
    def __init__(self, dataset_root: str):
        """
        Initializes the export validator.
        
        Args:
            dataset_root (str): The root directory for the dataset.
        """
        self.root = Path(dataset_root)
        self.results = []
        
    def validate_scene(self, scene_id: int, paths: dict) -> ValidationResult:
        """
        Validates internal constraints of an exported scene.
        
        Args:
            scene_id (int): The scene identifier.
            paths (dict): A dictionary of file paths generated by DatasetDirectoryBuilder.
            
        Returns:
            ValidationResult: Result containing pass status, errors, and warnings.
        """
        errors = []
        warnings = []
        
        # 1. Check existence
        for key, path in paths.items():
            if not Path(path).exists():
                errors.append(f"Missing file: {key} ({path})")
                
        # Fast fail if critical files are missing
        if errors:
            return ValidationResult(scene_id, False, errors, warnings)
            
        # 2, 3. Check resolutions (image, depth, mask)
        image = cv2.imread(str(paths["image"]))
        if image is None:
            errors.append(f"Could not read image: {paths['image']}")
        else:
            h_img, w_img = image.shape[:2]
            
            # Mask
            mask = cv2.imread(str(paths["mask"]), cv2.IMREAD_GRAYSCALE)
            if mask is None:
                errors.append(f"Could not read mask: {paths['mask']}")
            else:
                h_mask, w_mask = mask.shape[:2]
                if h_img != h_mask or w_img != w_mask:
                    errors.append(f"Resolution mismatch: Image ({w_img}x{h_img}) vs Mask ({w_mask}x{h_mask})")
                    
            # Depth
            depth_path = Path(paths["depth"])
            if depth_path.suffix == ".npy":
                try:
                    depth = np.load(str(depth_path))
                    h_depth, w_depth = depth.shape[:2]
                    if h_img != h_depth or w_img != w_depth:
                        errors.append(f"Resolution mismatch: Image ({w_img}x{h_img}) vs Depth ({w_depth}x{h_depth})")
                        
                    # 6. Check Depth map for NaN/Inf
                    if np.isnan(depth).any() or np.isinf(depth).any():
                        errors.append("Depth map contains NaN or Inf values")
                except Exception as e:
                    errors.append(f"Failed to load depth map: {e}")
                    
        # 4. Check YOLO format
        try:
            with open(paths["yolo_txt"], 'r', encoding='utf-8') as f:
                for line_idx, line in enumerate(f):
                    parts = line.strip().split()
                    if not parts:
                        continue
                    if len(parts) != 5:
                        errors.append(f"YOLO txt line {line_idx+1} does not have exactly 5 values")
                        continue
                    try:
                        vals = [float(v) for v in parts]
                        for idx, val in enumerate(vals[1:]): # skip class ID
                            if val < 0.0 or val > 1.0:
                                errors.append(f"YOLO txt line {line_idx+1} coordinate value out of [0, 1] range: {val}")
                    except ValueError:
                        errors.append(f"YOLO txt line {line_idx+1} contains non-float values")
        except Exception as e:
             errors.append(f"Failed to parse YOLO txt: {e}")
             
        # 5. Check COCO JSON format
        try:
            with open(paths["coco_json"], 'r', encoding='utf-8') as f:
                coco_data = json.load(f)
                if "images" not in coco_data:
                    errors.append("COCO JSON missing 'images' key")
                if "annotations" not in coco_data:
                    errors.append("COCO JSON missing 'annotations' key")
        except json.JSONDecodeError:
            errors.append("COCO JSON is not parseable")
        except Exception as e:
            errors.append(f"Failed to read COCO JSON: {e}")
            
        passed = len(errors) == 0
        return ValidationResult(scene_id, passed, errors, warnings)
        
    def validate_full_dataset(self, all_scene_ids: list, builder) -> dict:
        """
        Validates multiple scenes in the dataset.
        
        Args:
            all_scene_ids (list): List of all scene IDs to validate.
            builder: DatasetDirectoryBuilder instance used to compute path dict.
            
        Returns:
            dict: Evaluation summary including counts and pass rate.
        """
        self.results = []
        for scene_id in all_scene_ids:
            paths = builder.get_paths(scene_id)
            res = self.validate_scene(scene_id, paths)
            self.results.append(res)
            
        passed_count = sum(1 for r in self.results if r.passed)
        total = len(self.results)
        
        return {
            "total_scenes": total,
            "passed_count": passed_count,
            "pass_rate": (passed_count / total if total > 0 else 0.0)
        }
        
    def generate_report(self, output_path: str):
        """
        Saves a physical validation report to a text log.
        
        Args:
            output_path (str): The destination file for the report.
        """
        passed_count = sum(1 for r in self.results if r.passed)
        total = len(self.results)
        
        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(f"Validation Report\n=================\n")
            f.write(f"Total Scenes Evaluated: {total}\n")
            f.write(f"Passed: {passed_count}\n")
            if total > 0:
                f.write(f"Pass Rate: {passed_count / total * 100:.2f}%\n\n")
            else:
                f.write(f"Pass Rate: 0.00%\n\n")
                
            f.write("Scene Details:\n")
            for res in self.results:
                status = "PASS" if res.passed else "FAIL"
                f.write(f"Scene {res.scene_id:05d}: {status}\n")
                if res.errors:
                    for err in res.errors:
                        f.write(f"  [ERROR] {err}\n")
                if res.warnings:
                    for wrn in res.warnings:
                        f.write(f"  [WARN]  {wrn}\n")
                f.write("\n")
