"""
Dataset Manager module to wrap and coordinate the dataset export pipeline.
"""
import numpy as np
from pathlib import Path

from src.dataset.directory_builder import DatasetDirectoryBuilder
from src.dataset.metadata_writer import MetadataWriter
from src.dataset.export_validator import ExportValidator


class DatasetManager:
    """Coordinator that wraps directory building, metadata writing, and validation."""
    
    def __init__(self, root: str, scene_prefix: str = "scene", validate: bool = True):
        """
        Initializes the manager.
        
        Args:
            root (str): The dataset root path.
            scene_prefix (str): Prefix used for all scene files.
            validate (bool): Whether to run the validator post-export.
        """
        self.root = Path(root)
        self.scene_prefix = scene_prefix
        self.validate = validate
        
        self.builder = DatasetDirectoryBuilder(root, scene_prefix)
        self.builder.build()
        
        self.writer = MetadataWriter(root)
        self.validator = ExportValidator(root) if validate else None
        
        # Track scene ids exported during this session
        self.session_scenes = []

    def begin_export(self, scene_id: int) -> dict:
        """
        Gets the destination paths for a given scene export.
        
        Args:
            scene_id (int): Scene identifier.
            
        Returns:
            dict: Paths dictionary generated by the directory builder.
        """
        return self.builder.get_paths(scene_id)
        
    def finish_scene(self, scene_id: int, camera_params: dict, objects: list[dict], 
                     render_config: dict, rgb_image: np.ndarray, depth_map: np.ndarray, 
                     seg_mask: np.ndarray):
        """
        Finalizes an exported scene by creating metadata, performing validation, and updating stats.
        
        Args:
            scene_id (int): Scene identifier.
            camera_params (dict): Matrix/Position details of camera.
            objects (list[dict]): Objects present in the scene.
            render_config (dict): Background/Lighting configuration.
            rgb_image (np.ndarray): Saved RGB image data (assumed already saved to disk).
            depth_map (np.ndarray): Depth array mapping.
            seg_mask (np.ndarray): Segmentation mask mapping.
            
        Note:
            We assume the actual image rendering and saving to paths['image'], paths['depth'], etc.
            is handled by the caller before invoking this hook. This function registers the metadata
            and validates the saved outcomes.
        """
        # Register in CSV and JSON
        self.writer.write_scene(scene_id, camera_params, objects, render_config, depth_map)
        
        if scene_id not in self.session_scenes:
            self.session_scenes.append(scene_id)
        
        # Validate
        if self.validate:
            paths = self.begin_export(scene_id)
            res = self.validator.validate_scene(scene_id, paths)
            if not res.passed:
                print(f"Validation failed for scene {scene_id}!")
                for err in res.errors:
                    print(f"  [ERROR] {err}")
                    
        # Update README count
        self.builder.update_readme(len(self.session_scenes))
        
    def get_summary(self) -> dict:
        """
        Provides a summary of the dataset generation process.
        
        Returns:
            dict: Summary metrics.
        """
        summary = {
            "dataset_root": str(self.root),
            "total_scenes": len(self.session_scenes),
        }
        
        if self.validate:
            val_summary = self.validator.validate_full_dataset(self.session_scenes, self.builder)
            report_path = self.root / "validation_report.txt"
            self.validator.generate_report(str(report_path))
            summary["pass_rate"] = val_summary["pass_rate"]
            
        return summary
